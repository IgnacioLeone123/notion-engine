# ── Palabras clave para detectar callouts de bienvenida ──
_WELCOME_KEYWORDS = ["bienvenido", "welcome", "hola", "dashboard", "panel"]


def guess_icon(db_name: str) -> str:
    """Elige un emoji para la base de datos según su nombre."""
    name_lower = db_name.lower()
    for keyword, icon in DATABASE_ICONS.items():
        if keyword in name_lower:
            return icon
    return "📋"


def enhance_block_emoji(content: str, block_type: str) -> str:
    """Agrega emoji al inicio del contenido si no tiene uno."""
    has_emoji = any(c in "🌟⭐✨💡🎯🏆📊📈📉🎨🚀💼💰✅📝🔍🛠️🎯🏆🥇💎🌟⭐✨💡🎯🏆🥇💎🌟" for c in content[:5])
    if has_emoji:
        return content
    if block_type in ("heading_1", "heading_2", "heading_3", "callout"):
        if any(kw in content.lower() for kw in _WELCOME_KEYWORDS):
            return f"🚀 {content}"
        return content
    return content
